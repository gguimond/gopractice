package main

import (
    "fmt"
    "sync"
)

func main() {
    // Set up the pipeline.
    c := gen(2, 3)
    c1 := sq(c)
    c2 := sq(c)

    for n := range merge(c1, c2) {
        fmt.Println(n)
    }
}

func gen(nums ...int) <-chan int {
    out := make(chan int, len(nums))
    for _, n := range nums {
        out <- n
    }
    close(out)
    return out
}

func sq(in <-chan int) <-chan int {
    out := make(chan int)
    go func() {
        for n := range in {
            out <- n * n
        }
        close(out)
    }()
    return out
}

func merge(cs ...<-chan int) <-chan int{
    var wg sync.WaitGroup
    out := make(chan int, 1)

    output := func(c <-chan int) {
        for n := range c {
            out <- n
        }
        wg.Done()
    }

    wg.Add(len(cs))

    for _, c := range cs {
        go output(c)
    }

    go func() {
        wg.Wait()
        close(out)
    }()

    return out
}